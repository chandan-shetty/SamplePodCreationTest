import Foundation

public final class AdditionHandler {
    
    let name = "AdditionHandler"
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
}
